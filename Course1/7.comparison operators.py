"""
比較運算子的用法，比如常見的大於、小於或等於...等，都是比較運算子的一種喔~
等於:==、不等於:!=、

"""
z1,z2,z3,z4,z5=10,20,30,25,10#這邊的=是指定
#多個變數的值也可以用這句一次設定
if z1 >= z2:
    print("if True")
elif z1 == z3:
    print("elif1 true")
elif z1 == z4:
    print("elif2 true")
elif z1 <= z5:
    print("elif3 true")
elif z1 <= z2:
    print("elif4 true")